package com.wasselni.common.utils.map.xml.cs;

public class Mapping {

	private ValuesFrom ValuesFrom;
	private ValuesTo ValuesTo;
	private String Description;

	public ValuesFrom getValuesFrom() {
		return ValuesFrom;
	}

	public void setValuesFrom(ValuesFrom valuesFrom) {
		ValuesFrom = valuesFrom;
	}

	public ValuesTo getValuesTo() {
		return ValuesTo;
	}

	public void setValuesTo(ValuesTo valuesTo) {
		ValuesTo = valuesTo;
	}

	public String getDescription() {
		return Description;
	}

	public void setDescription(String description) {
		Description = description;
	}

}
